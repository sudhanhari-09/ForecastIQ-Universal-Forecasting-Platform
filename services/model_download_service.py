def _detect_framework(model_obj):
    type_str = str(type(model_obj)).lower()
    if 'xgboost' in type_str:
        return 'xgboost'
    if 'lightgbm' in type_str or 'lgbm' in type_str:
        return 'lightgbm'
    if 'catboost' in type_str:
        return 'catboost'
    if 'keras' in type_str or 'tensorflow' in type_str:
        return 'tensorflow'
    if 'statsmodels' in type_str or 'arima' in type_str or 'sarimax' in type_str or 'exponentialsmoothing' in type_str:
        return 'statsmodels'
    if 'sklearn' in type_str or 'linearregression' in type_str or 'randomforest' in type_str:
        return 'sklearn'
    if 'prophet' in type_str:
        return 'prophet'
    return 'unknown'


