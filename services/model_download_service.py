def _trim_statsmodels_model(model_obj):
    _MAX_OBS = 100

    if hasattr(model_obj, 'filter_results'):
        fr = model_obj.filter_results
        _ARRAYS = [
            'filtered_state', 'filtered_state_cov',
            'forecasts_error',
            'standardized_forecasts_error', 'forecasts_error_cov',
            'kalman_gain',
        ]
        for attr_name in _ARRAYS:
            try:
                arr = getattr(fr, attr_name, None)
            except Exception:
                continue
            if isinstance(arr, np.ndarray) and arr.ndim >= 1 and arr.shape[-1] > _MAX_OBS:
                try:
                    slicing = [slice(None)] * (arr.ndim - 1) + [slice(-_MAX_OBS, None)]
                    setattr(fr, attr_name, arr[tuple(slicing)].copy())
                except Exception:
                    pass

    if hasattr(model_obj, 'smoother_results'):
        sr = model_obj.smoother_results
        for attr_name in ['smoothed_state', 'smoothed_state_cov', 'smoothed_state_autocov',
                           'scaled_smoothed_estimator', 'scaled_smoothed_estimator_cov',
                           'smoothing_error', 'smoothed_measurement_disturbance',
                           'smoothed_state_disturbance', 'smoothed_measurement_disturbance_cov',
                           'smoothed_state_disturbance_cov']:
            try:
                arr = getattr(sr, attr_name, None)
            except Exception:
                continue
            if isinstance(arr, np.ndarray) and arr.ndim >= 1 and arr.shape[-1] > _MAX_OBS:
                try:
                    slicing = [slice(None)] * (arr.ndim - 1) + [slice(-_MAX_OBS, None)]
                    setattr(sr, attr_name, arr[tuple(slicing)].copy())
                except Exception:
                    pass

    for attr_name in ('level', 'trend', 'seasonal'):
        try:
            arr = getattr(model_obj, attr_name, None)
        except Exception:
            continue
        if isinstance(arr, np.ndarray) and arr.ndim >= 1 and arr.shape[0] > _MAX_OBS:
            try:
                setattr(model_obj, attr_name, arr[-_MAX_OBS:].copy())
            except Exception:
                pass


def save_trained_model(model_obj, dataset_id, model_name, target_column, date_column,
                       forecast_horizon, feature_columns, training_samples, testing_samples,
                       dataset_name, trained_model_extra=None):
    model_dir = _get_model_dir(dataset_id)
    os.makedirs(model_dir, exist_ok=True)

    framework = _detect_framework(model_obj)
    versions = _get_framework_versions()
    timestamp = datetime.datetime.utcnow().strftime('%Y-%m-%d %H:%M:%S UTC')

    metadata = {
        'dataset_name': dataset_name,
        'forecast_model': model_name,
        'target_column': target_column,
        'date_column': date_column,
        'forecast_horizon': forecast_horizon,
        'training_samples': training_samples,
        'testing_samples': testing_samples,
        'feature_columns': feature_columns,
        'feature_count': len(feature_columns),
        'training_timestamp': timestamp,
        'framework': framework,
        'framework_versions': versions,
        'model_version': '1.0.0',
    }

    if framework == 'tensorflow':
        keras_path = os.path.join(model_dir, 'model.keras')
        model_obj.save(keras_path)
    elif framework == 'xgboost':
        native_path = os.path.join(model_dir, 'model.json')
        model_obj.save_model(native_path)
        pkl_path = os.path.join(model_dir, 'model.pkl')
        joblib.dump(model_obj, pkl_path)
    elif framework == 'lightgbm':
        native_path = os.path.join(model_dir, 'model.txt')
        model_obj.save_model(native_path)
        pkl_path = os.path.join(model_dir, 'model.pkl')
        joblib.dump(model_obj, pkl_path)
    elif framework == 'catboost':
        native_path = os.path.join(model_dir, 'model.cbm')
        model_obj.save_model(native_path)
        pkl_path = os.path.join(model_dir, 'model.pkl')
        try:
            joblib.dump(model_obj, pkl_path)
        except Exception:
            pass
    elif framework == 'prophet':
        from prophet.serialize import model_to_json
        prophet_path = os.path.join(model_dir, 'model.json')
        with open(prophet_path, 'w') as f:
            f.write(model_to_json(model_obj))
        pkl_path = os.path.join(model_dir, 'model.pkl')
        try:
            joblib.dump(model_obj, pkl_path)
        except Exception:
            pass
    else:
        pkl_path = os.path.join(model_dir, 'model.pkl')
        if framework == 'statsmodels':
            _trim_statsmodels_model(model_obj)
        joblib.dump(model_obj, pkl_path)

    preprocessor = {
        'date_column': date_column,
        'target_column': target_column,
        'feature_columns': feature_columns,
        'forecast_horizon': forecast_horizon,
        'is_time_series': len(feature_columns) == 0,
    }
    if trained_model_extra:
        preprocessor['numeric_features'] = trained_model_extra.get('numeric_features', [])
        preprocessor['date_features_created'] = trained_model_extra.get('date_features_created', False)
    joblib.dump(preprocessor, os.path.join(model_dir, 'preprocessor.pkl'))

    with open(os.path.join(model_dir, 'feature_columns.json'), 'w', encoding='utf-8') as f:
        json.dump(feature_columns, f, indent=2)

    with open(os.path.join(model_dir, 'metadata.json'), 'w', encoding='utf-8') as f:
        json.dump(metadata, f, indent=2)

    readme = _generate_readme(metadata)
    with open(os.path.join(model_dir, 'README.txt'), 'w', encoding='utf-8') as f:
        f.write(readme)

    requirements = _generate_requirements(framework)
    with open(os.path.join(model_dir, 'requirements.txt'), 'w', encoding='utf-8') as f:
        f.write(requirements)

    return model_dir


def validate_model_package(dataset_id):
    model_dir = _get_model_dir(dataset_id)
    if not os.path.exists(model_dir):
        return False, 'No trained model found. Run forecasting first.'

    required_files = ['model.pkl', 'preprocessor.pkl', 'feature_columns.json',
                      'metadata.json', 'README.txt', 'requirements.txt']

    framework = 'unknown'
    meta_path = os.path.join(model_dir, 'metadata.json')
    if os.path.exists(meta_path):
        try:
            with open(meta_path, 'r') as f:
                meta = json.load(f)
            framework = meta.get('framework', 'unknown')
        except Exception:
            pass

    if framework == 'tensorflow':
        required_files.remove('model.pkl')
        required_files.append('model.keras')
    elif framework == 'prophet':
        required_files.remove('model.pkl')
        required_files.append('model.json')

    for fname in required_files:
        fpath = os.path.join(model_dir, fname)
        if not os.path.exists(fpath):
            return False, f'Required file missing: {fname}'
        try:
            with open(fpath, 'rb') as f:
                f.read(1)
        except Exception as e:
            return False, f'File unreadable: {fname} ({str(e)})'

    try:
        with open(meta_path, 'r') as f:
            meta = json.load(f)
        required_meta = ['forecast_model', 'training_timestamp', 'target_column']
        for field in required_meta:
            if field not in meta:
                return False, f'Metadata field missing: {field}'
    except Exception as e:
        return False, f'Metadata file corrupt: {str(e)}'

    return True, None


def get_trained_model_info(dataset_id):
    model_dir = _get_model_dir(dataset_id)
    if not os.path.exists(model_dir):
        return None, None, 'No trained model found.'

    meta_path = os.path.join(model_dir, 'metadata.json')
    if not os.path.exists(meta_path):
        return None, None, 'Metadata file not found.'

    with open(meta_path, 'r') as f:
        meta = json.load(f)

    framework = meta.get('framework', 'unknown')
    model_name = meta.get('forecast_model', 'Model')

    if framework == 'tensorflow':
        model_file = 'model.keras'
        ext = '.keras'
    else:
        model_file = 'model.pkl'
        ext = '.pkl'

    model_path = os.path.join(model_dir, model_file)
    if not os.path.exists(model_path):
        return None, None, f'Model file not found: {model_file}'

    download_name = f'{model_name}_Model{ext}'
    return model_path, download_name, None


def create_model_zip(dataset_id, dataset_name=None):
    valid, err = validate_model_package(dataset_id)
    if not valid:
        return None, err

    model_dir = _get_model_dir(dataset_id)

    meta_path = os.path.join(model_dir, 'metadata.json')
    with open(meta_path, 'r') as f:
        meta = json.load(f)
    model_name = meta.get('forecast_model', 'Model')
    base = dataset_name or f'model_{dataset_id}'
    zip_name = f'{model_name}_Model.zip'

    buf = io.BytesIO()
    with zipfile.ZipFile(buf, 'w', zipfile.ZIP_DEFLATED) as zf:
        for root, dirs, files in os.walk(model_dir):
            for fname in files:
                fpath = os.path.join(root, fname)
                arcname = os.path.relpath(fpath, model_dir)
                zf.write(fpath, arcname)

    buf.seek(0)
    return buf, None
